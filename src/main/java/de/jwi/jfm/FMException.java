package de.jwi.jfm;

public class FMException extends Exception
{

    public FMException()
    {
        super();
    }

    public FMException(String message)
    {
        super(message);
    }

    public FMException(Throwable cause)
    {
        super(cause);
    }

    public FMException(String message, Throwable cause)
    {
        super(message, cause);
    }

}
